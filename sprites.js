function pixText(str,x,y,c='#fff'){
  ctx.fillStyle=c;str=(''+str).toUpperCase();
  for(let i=0;i<str.length;i++){
    const ch=FONT[str[i]]||FONT[' '],rows=ch.split(';');
    for(let r=0;r<5;r++)for(let cc=0;cc<3;cc++)
      if(rows[r][cc]==='1')ctx.fillRect(x+i*4+cc,y+r,1,1);
  }
}
function pixBig(str,x,y,c){
  ctx.save();ctx.translate(x,y);ctx.scale(2,2);pixText(str,0,0,c);ctx.restore();
}
function pixW(str){return str.length*4;}

// ── SPRITES ─────────────────────────────────────────────
function spr(rows,px,py,color){
  ctx.fillStyle=color;
  for(let r=0;r<rows.length;r++)
    for(let c=0;c<rows[r].length;c++)
      if(rows[r][c]==='1')ctx.fillRect(px+c,py+r,1,1);
}

